package com.huangjn.blogbackstage.modules.content.pojo;

public class Music {
    private int musicId;
    private String musicName;
    private String musicUrl;
    private String musicContent;
    private String musicPhoto;

    public int getMusicId() {
        return musicId;
    }

    public void setMusicId(int musicId) {
        this.musicId = musicId;
    }

    public String getMusicName() {
        return musicName;
    }

    public void setMusicName(String musicName) {
        this.musicName = musicName;
    }

    public String getMusicUrl() {
        return musicUrl;
    }

    public void setMusicUrl(String musicUrl) {
        this.musicUrl = musicUrl;
    }

    public String getMusicContent() {
        return musicContent;
    }

    public void setMusicContent(String musicContent) {
        this.musicContent = musicContent;
    }

    public String getMusicPhoto() {
        return musicPhoto;
    }

    public void setMusicPhoto(String musicPhoto) {
        this.musicPhoto = musicPhoto;
    }
}
